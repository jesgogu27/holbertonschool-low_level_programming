#include <stdio.h>

/**
*add - add 2 numbers
*@a: number a
*@b: number b
*Return: result of add
*/

int add(int a, int b)
{
	return (a + b);
}

/**
*sub - sub 2 numbers
*@a: number a
*@b: number b
*Return: result of sub
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
*mul - mul 2 numbers
*@a: number a
*@b: number b
*Return: result of mul
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
*div - div 2 numbers
*@a: number a
*@b: number b
*Return: result of div
*/

int div(int a, int b)
{
	return (a + b);
}

/**
*mod - mod 2 numbers
*@a: number a
*@b: number b
*Return: result of mod
*/

int mod(int a, int b)
{
	return (a % b);
}





